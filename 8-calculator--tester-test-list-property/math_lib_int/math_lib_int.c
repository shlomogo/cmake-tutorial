#include <stdint.h>
#include "include/math_lib_int.h"

int64_t
math_lib_int_add(int32_t a, int32_t b)
{
    return a + b;
}

int64_t
math_lib_int_sub(int32_t a, int32_t b)
{
    return a - b;
}

int64_t
math_lib_int_mul(int32_t a, int32_t b)
{
    return a * b;
}

int64_t
math_lib_int_div(int32_t a, int32_t b)
{
    return a / b;
}
