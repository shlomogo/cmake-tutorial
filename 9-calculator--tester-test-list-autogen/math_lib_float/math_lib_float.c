#include <stdint.h>
#include "include/math_lib_float.h"

double
math_lib_float_add(float a, float b)
{
    return a + b;
}

double
math_lib_float_sub(float a, float b)
{
    return a - b;
}

double
math_lib_float_mul(float a, float b)
{
    return a * b;
}

double
math_lib_float_div(float a, float b)
{
    return a / b;
}
