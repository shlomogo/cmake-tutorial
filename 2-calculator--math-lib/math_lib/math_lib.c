#include <stdint.h>
#include "math_lib.h"

int64_t
math_lib_add(int32_t a, int32_t b)
{
    return a + b;
}

int64_t
math_lib_sub(int32_t a, int32_t b)
{
    return a - b;
}

int64_t
math_lib_mul(int32_t a, int32_t b)
{
    return a * b;
}

int64_t
math_lib_div(int32_t a, int32_t b)
{
    return a / b;
}
